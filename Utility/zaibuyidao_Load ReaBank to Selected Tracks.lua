function selectReaBankFile()
    local retval, filePath = reaper.GetUserFileNameForRead("", "Select ReaBank File", ".reabank")
    if retval then
        return filePath
    else
        return nil
    end
end

function applyReaBankToTrack(track, reabankPath)
    if not track then return false end

    local ret, trackStateChunk = reaper.GetTrackStateChunk(track, "", false) -- 获取当前轨道状态
    if ret and trackStateChunk then
        -- 修改轨道状态以包含Reabank路径
        local bankString = "MIDIBANKPROGFN \"" .. reabankPath .. "\"\n"
        if string.find(trackStateChunk, "MIDIBANKPROGFN") then
            trackStateChunk = string.gsub(trackStateChunk, "MIDIBANKPROGFN \"[^\"]*\"", bankString)
        else
            trackStateChunk = trackStateChunk:gsub("<TRACK", "<TRACK\n" .. bankString)
        end

        return reaper.SetTrackStateChunk(track, trackStateChunk, false)
    end
    return false
end

local reabankPath = selectReaBankFile()
if not reabankPath then return end

reaper.Undo_BeginBlock()
reaper.PreventUIRefresh(1)

local numSelectedTracks = reaper.CountSelectedTracks(0)
if numSelectedTracks > 0 then
    for i = 0, numSelectedTracks - 1 do
        local track = reaper.GetSelectedTrack(0, i)
        if track then
            if not applyReaBankToTrack(track, reabankPath) then
                Msg("Failed to apply ReaBank to a track.")
            end
        end
    end
end

reaper.PreventUIRefresh(-1)
reaper.Undo_EndBlock("Load ReaBank to Selected Tracks", -1)
